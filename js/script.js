function loadHospitals() {
    const hospitalGrid = document.getElementById('hospital-grid');
    if (!hospitalGrid) return;
    
    const hospitals = hospitalData[currentHospitalType];
    hospitalGrid.innerHTML = '';
    
    hospitals.forEach(hospital => {
        const hospitalCard = document.createElement('div');
        hospitalCard.className = 'hospital-card';
        hospitalCard.onclick = () => showHospitalDetails(hospital);
        
        hospitalCard.innerHTML = `
            <div class="hospital-icon">
                <img src="./images/hospital.png" alt="Hospital" class="hospital-icon-img">
            </div>
            <div class="hospital-name">${hospital.name}</div>
        `;
        
        hospitalGrid.appendChild(hospitalCard);
    });
}

function showHospitalDetails(hospital) {
    currentHospital = hospital;
    showPage('hospital-details');
    
    // Update hospital details
    document.getElementById('hospital-name').textContent = hospital.name;
    document.getElementById('hospital-address').textContent = hospital.address;
    document.getElementById('hospital-phone').textContent = hospital.phone;
    
    // Load departments
    loadDepartments(hospital.departments);
}

function loadDepartments(departments) {
    const departmentsGrid = document.getElementById('departments-grid');
    if (!departmentsGrid) return;
    
    departmentsGrid.innerHTML = '';
    
    departments.forEach(department => {
        const departmentCard = document.createElement('div');
        departmentCard.className = 'department-card';
        departmentCard.onclick = () => showDepartmentDetails(department);
        
        departmentCard.innerHTML = `
            <div class="department-icon">${department.icon}</div>
            <div class="department-name">${department.name}</div>
        `;
        
        departmentsGrid.appendChild(departmentCard);
    });
}

function showDepartmentDetails(department) {
    currentDepartment = department;
    showPage('department-details');
    
    // Update department details
    document.getElementById('department-name').textContent = department.name;
    document.getElementById('department-description').textContent = department.description;
    
    // Update services list
    const servicesList = document.getElementById('services-list');
    if (servicesList) {
        servicesList.innerHTML = '';
        department.services.forEach(service => {
            const li = document.createElement('li');
            li.textContent = service;
            servicesList.appendChild(li);
        });
    }
    
    // Update requirements list
    const requirementsList = document.getElementById('requirements-list');
    if (requirementsList) {
        requirementsList.innerHTML = '';
        department.requirements.forEach(requirement => {
            const li = document.createElement('li');
            li.textContent = requirement;
            requirementsList.appendChild(li);
        });
    }
}

